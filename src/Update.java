    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
       if(search.getText().trim().equals("")||uitem_no.getText().trim().equals("")||uprod_name.getText().trim().equals("")||jDateChooser5.getDate().equals("")||u1price.getText().trim().equals("")||uqty.getText().trim().equals("")||utotal.getText().trim().equals(""))
       { 
        JOptionPane.showMessageDialog(this,"fill the values");
        }
        else{
        Connection con=DbConnection.connect();
        PreparedStatement ps =null;
        ResultSet rs=null;
                try{
                        String sql= "update purchase set Product_Id=?, Product_Name=?,Date=?,Quantity=?,PerPrice=?,totalprice=? where Product_Id=0"+search.getText();
                        ps=con.prepareStatement(sql);
                        ps.setInt(1, Integer.parseInt(uitem_no.getText()));
                        ps.setString(2, uprod_name.getText());
                        SimpleDateFormat sdf=new SimpleDateFormat("yyyy-MM-dd");
                        String datetime = sdf.format(jDateChooser5.getDate());
                        ps.setString(3, datetime);
                        ps.setInt(4,Integer.parseInt(uqty.getText()));
                        ps.setInt(5,Integer.parseInt(u1price.getText()));
                        ps.setInt(6,Integer.parseInt(utotal.getText()));
                        ps.executeUpdate();
                        JOptionPane.showMessageDialog(null,"Update Product");
                        ps.close();
                         con.close();
                        dispose();
                    }catch(Exception ex)
                    {
                        JOptionPane.showMessageDialog(null,"put the valid values");
                    }finally{
                            try{
                            ps.close();
                            rs.close();
                            con.close();}
                            catch(SQLException ex){JOptionPane.showMessageDialog(null,ex); }
                        }
                                }
        
    }//GEN-LAST:event_jButton1ActionPerformed


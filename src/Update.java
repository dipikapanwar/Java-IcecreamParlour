    private void searchKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchKeyPressed
        // TODO add your handling code here:
         Connection con=DbConnection.connect();
        PreparedStatement ps =null;
        ResultSet rs=null;
        if(evt.getKeyCode()==KeyEvent.VK_ENTER){ 
         try{
            String sql="select * from purchase where Product_Id="+search.getText();
            ps=con.prepareStatement(sql);
            rs=ps.executeQuery();
            if(rs.next()){
                     
                String add1=rs.getString("Product_Id");
                uitem_no.setText(add1);
                String add2=rs.getString("Product_Name"); 
                uprod_name.setText(add2);
                String dateValue = rs.getString("Date"); // What ever column
                java.util.Date date = new SimpleDateFormat("yyyy-MM-dd").parse(dateValue);
                jDateChooser5.setDate(date);
                String add4=rs.getString("Quantity");
                uqty.setText(add4);
                String add5=rs.getString("PerPrice");
                u1price.setText(add5);
                String add6=rs.getString("totalprice");
                utotal.setText(add6);
                }
                else{JOptionPane.showMessageDialog(null,"Product-ID is Wrong");}
                }
        catch(Exception ex){JOptionPane.showMessageDialog(null,ex);}
         finally{
        try{
        ps.close();
        rs.close();
        con.close();}
        catch(SQLException ex){JOptionPane.showMessageDialog(null,ex); }
    }
        }
    }//GEN-LAST:event_searchKeyPressed

